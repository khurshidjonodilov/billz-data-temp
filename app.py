def get_label_from_openai(message: str) -> str:
    """Отправляет сообщение в OpenAI и получает подходящую метку"""
    labels_str = "\n".join(f"- {label}" for label in LABELS)
    
    response = requests.post(
        "https://api.openai.com/v1/chat/completions",
        headers={
            "Authorization": f"Bearer {OPENAI_API_KEY}",
            "Content-Type": "application/json"
        },
        json={
            "model": "gpt-4o-mini",
            "messages": [
                {
                    "role": "system",
                    "content": f"""Ты помощник службы поддержки BILLZ. 
Твоя задача — выбрать ОДНУ наиболее подходящую метку из списка для вопроса клиента.
Отвечай ТОЛЬКО точным названием метки из списка, без объяснений.
Если ни одна метка не подходит — ответь "Вопрос".

Список меток:
{labels_str}"""
                },
                {
                    "role": "user", 
                    "content": f"Вопрос клиента: {message}"
                }
            ],
            "max_tokens": 100,
            "temperature": 0.1
        }
    )
    
    if response.status_code == 200:
        label = response.json()["choices"][0]["message"]["content"].strip()
        # Проверяем что метка из нашего списка
        if label in LABELS:
            return label
        # Ищем частичное совпадение
        for l in LABELS:
            if label.lower() in l.lower() or l.lower() in label.lower():
                return l
        return "Вопрос"
    return "Вопрос"


def add_label_to_case(case_id: str, label: str):
    """Добавляет метку к обращению в Омнидеске"""
    url = f"https://{OMNIDESK_DOMAIN}/api/cases/{case_id}.json"
    
    response = requests.put(
        url,
        auth=(OMNIDESK_API_KEY, "x"),
        json={
            "case": {
                "labels": [label]
            }
        }
    )
    return response.status_code


@app.route("/webhook", methods=["POST"])
def webhook():
    """Принимает вебхук от Омнидеска"""
    data = request.json
    
    if not data:
        return jsonify({"error": "No data"}), 400
    
    # Получаем ID обращения и текст сообщения
    case_id = data.get("case_id") or data.get("id")
    message = data.get("content") or data.get("message") or data.get("description", "")
    
    if not case_id or not message:
        return jsonify({"error": "Missing case_id or message"}), 400
    
    # Определяем метку через OpenAI
    label = get_label_from_openai(message)
    
    # Проставляем метку в Омнидеск
    status = add_label_to_case(case_id, label)
    
    return jsonify({
        "case_id": case_id,
        "label": label,
        "omnidesk_status": status
    })


