# Файл для хранения уже обработанных обращений
PROCESSED_FILE = "/tmp/processed_cases.json"


def load_processed():
    try:
        with open(PROCESSED_FILE, "r") as f:
            return set(json.load(f))
    except:
        return set()


def save_processed(processed):
    with open(PROCESSED_FILE, "w") as f:
        json.dump(list(processed), f)


def get_new_cases():
    """Получает новые обращения от ИИ-агента без меток"""
    url = f"https://{OMNIDESK_DOMAIN}/api/cases.json"
    params = {
        "status": "open",
        "page": 1,
        "limit": 50
    }
    
    try:
        response = requests.get(
            url,
            auth=(OMNIDESK_EMAIL, OMNIDESK_API_KEY),
            params=params,
            timeout=10
        )
        if response.status_code == 200:
            data = response.json()
            cases = data.get("cases", [])
            # Фильтруем только обращения ИИ-агента (staff_id = 10220143)
            return [
                c["case"] for c in cases
                if isinstance(c, dict) and "case" in c
                and str(c["case"].get("staff_id", "")) == "10220143"
            ]
        else:
            print(f"API error: {response.status_code} - {response.text[:200]}")
    except Exception as e:
        print(f"Error getting cases: {e}")
    return []


def get_label_from_openai(message: str) -> str:
    """Определяет метку через OpenAI"""
    labels_str = "\n".join(f"- {label}" for label in LABELS)
    
    try:
        response = requests.post(
            "https://api.openai.com/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {OPENAI_API_KEY}",
                "Content-Type": "application/json"
            },
            json={
                "model": "gpt-4o-mini",
                "messages": [
                    {
                        "role": "system",
                        "content": f"""Ты помощник службы поддержки BILLZ.
Выбери ОДНУ наиболее подходящую метку из списка для вопроса клиента.
Отвечай ТОЛЬКО точным названием метки из списка, без объяснений.
Если ни одна метка не подходит — ответь "Вопрос".

Список меток:
{labels_str}"""
                    },
                    {
                        "role": "user",
                        "content": f"Вопрос клиента: {message}"
                    }
                ],
                "max_tokens": 100,
                "temperature": 0.1
            },
            timeout=15
        )
        
        if response.status_code == 200:
            label = response.json()["choices"][0]["message"]["content"].strip()
            if label in LABELS:
                return label
            for l in LABELS:
                if label.lower() in l.lower() or l.lower() in label.lower():
                    return l
    except Exception as e:
        print(f"OpenAI error: {e}")
    
    return "Вопрос"


def add_label_to_case(case_id: str, label: str):
    """Добавляет метку к обращению"""
    url = f"https://{OMNIDESK_DOMAIN}/api/cases/{case_id}.json"
    
    try:
        response = requests.put(
            url,
            auth=(OMNIDESK_EMAIL, OMNIDESK_API_KEY),
            json={"case": {"labels": [label]}},
            timeout=10
        )
        print(f"Case {case_id} → label '{label}' → status {response.status_code}")
        return response.status_code
    except Exception as e:
        print(f"Error adding label: {e}")
        return 0


def process_cases():
    """Основной цикл обработки"""
    processed = load_processed()
    cases = get_new_cases()
    
    new_count = 0
    for case in cases:
        case_id = str(case.get("case_id", ""))
        if not case_id or case_id in processed:
            continue
        
        # Если уже есть метки — пропускаем
        labels = case.get("labels", [])
        if labels:
            processed.add(case_id)
            continue
        
        # Получаем текст обращения
        message = case.get("subject", "") + " " + case.get("description", "")
        message = message.strip()
        
        if not message:
            continue
        
        # Определяем метку
        label = get_label_from_openai(message)
        
        # Проставляем метку
        add_label_to_case(case_id, label)
        processed.add(case_id)
        new_count += 1
        
        # Пауза чтобы не перегружать API
        time.sleep(1)
    
    save_processed(processed)
    print(f"[{datetime.now().strftime('%H:%M:%S')}] Processed {new_count} new cases")


if __name__ == "__main__":
    print("BILLZ Label Bot started!")
    print(f"Polling every {POLL_INTERVAL} seconds...")
    
    while True:
        try:
            process_cases()
        except Exception as e:
            print(f"Error in main loop: {e}")
        
        time.sleep(POLL_INTERVAL)
